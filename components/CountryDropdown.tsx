export default function CountryDropdown({ value, onChange, placeholder = "Select country" }: CountryDropdownProps) {
  const [isOpen, setIsOpen] = useState(false)
  const [searchTerm, setSearchTerm] = useState('')
  const [filteredCountries, setFilteredCountries] = useState(countries)
  const dropdownRef = useRef<HTMLDivElement>(null)

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target as Node)) {
        setIsOpen(false)
      }
    }

    document.addEventListener('mousedown', handleClickOutside)
    return () => {
      document.removeEventListener('mousedown', handleClickOutside)
    }
  }, [])

  useEffect(() => {
    const filtered = countries.filter(country =>
      country.name.toLowerCase().includes(searchTerm.toLowerCase())
    )
    setFilteredCountries(filtered)
  }, [searchTerm])

  const handleSelect = (country: { code: string; name: string }) => {
    onChange(country.code)
    setIsOpen(false)
    setSearchTerm('')
  }

  const displayValue = value ? countries.find(c => c.code === value)?.name || placeholder : placeholder

  return (
    <div style={{ position: 'relative', width: '100%' }}>
      <div
        onClick={() => setIsOpen(!isOpen)}
        style={{
          width: '100%',
          padding: '12px',
          borderRadius: '8px',
          border: '1px solid var(--border2)',
          background: 'var(--card2)',
          fontSize: '14px',
          cursor: 'pointer',
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'space-between'
        }}
      >
        <span style={{ color: value ? 'var(--text)' : 'var(--text3)' }}>
          {displayValue}
        </span>
        <span style={{ color: 'var(--text3)' }}>▼</span>
      </div>

      {isOpen && (
        <div
          ref={dropdownRef}
          style={{
            position: 'absolute',
            top: '100%',
            left: '0',
            right: '0',
            zIndex: 1000,
            background: 'var(--card)',
            border: '1px solid var(--border)',
            borderRadius: '8px',
            maxHeight: '200px',
            overflow: 'auto',
            boxShadow: 'var(--shadow-lg)'
          }}
        >
          <input
            type="text"
            placeholder="Search countries..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            onClick={(e) => e.stopPropagation()}
            style={{
              width: '100%',
              padding: '8px 12px',
              border: 'none',
              borderBottom: '1px solid var(--border2)',
              outline: 'none',
              fontSize: '14px'
            }}
          />
          <div style={{ maxHeight: '150px', overflow: 'auto' }}>
            {filteredCountries.slice(0, 10).map(country => (
              <div
                key={country.code}
                onClick={() => handleSelect(country)}
                style={{
                  padding: '8px 12px',
                  cursor: 'pointer',
                  borderBottom: '1px solid var(--border2)',
                  background: country.code === value ? 'var(--green-tint)' : 'transparent',
                  color: country.code === value ? 'var(--green)' : 'var(--text)',
                  fontSize: '14px'
                }}
                onMouseEnter={(e) => e.stopPropagation()}
              >
                {country.name}
              </div>
            ))}
            {filteredCountries.length > 10 && (
              <div style={{ 
                padding: '8px 12px', 
                textAlign: 'center', 
                color: 'var(--text3)', 
                fontSize: '12px',
                fontStyle: 'italic'
              }}>
                ...and {filteredCountries.length - 10} more
              </div>
            )}
          </div>
        </div>
      )}
    </div>
  )
}
